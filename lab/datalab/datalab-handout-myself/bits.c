/* 
 * isPositive - return 1 if x > 0, return 0 otherwise 
 *   Example: isPositive(-1) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 8
 *   Rating: 3
 */
int isPositive(int x) {
	/* !x=1 only if x=0 */
	return !((x>>31)|(!x));
}
/* 
 * isLessOrEqual - if x <= y  then return 1, else return 0 
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLessOrEqual(int x, int y) {
	int ysign=y>>31;
	int difsign=ysign^(x>>31);
	int sub=y+~x+1;
	return ((~difsign)&!(sub>>31))|(difsign&(!ysign));
}
/*
 * ilog2 - return floor(log base 2 of x), where x > 0
 *   Example: ilog2(16) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 90
 *   Rating: 4
 */
int ilog2(int x) {
	int s1,s2,s3,s4,s5;
	s1=(!!(x>>16))<<4;
	x>>=s1;
	s2=(!!(x>>8))<<3;
	x>>=s2;
	s3=(!!(x>>4))<<2;
	x>>=s3;
	s4=(!!(x>>2))<<1;
	x>>=s4;
	s5=!!(x>>1);
	x>>=s5;
	return s1+s2+s3+s4+s5;
}
/* 
 * float_neg - Return bit-level equivalent of expression -f for
 *   floating point argument f.
 *   Both the argument and result are passed as unsigned int's, but
 *   they are to be interpreted as the bit-level representations of
 *   single-precision floating point values.
 *   When argument is NaN, return argument.
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 10
 *   Rating: 2
 */
unsigned float_neg(unsigned uf) {
	unsigned sign=uf>>31;
	unsigned t=uf&0x7fffffff;
	if(t>0x7f800000) return uf;
	return (~sign<<31)|t;
}
/* 
 * float_i2f - Return bit-level equivalent of expression (float) x
 *   Result is returned as unsigned int, but
 *   it is to be interpreted as the bit-level representation of a
 *   single-precision floating point values.
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
unsigned float_i2f(int x) {
	unsigned i,frac,round;
	unsigned inc=0,E=0,bias=127,sign=0,temp=x;

	/* special case */
	if(x==0)
		return 0;
	/* normalized numbers transformed to positive values*/
	if(x<0)
	{
		temp=-x;
		sign=0x80000000;
	}
	/* calculate the exponent */
	for(i=temp;i>1;i>>=1,E+=1);
	frac=temp<<(32-E);
	/* calculate the 23rd-32nd bits to round */
	round=frac&0x3ff;
	if(round>=0x300||(round<0x200&&round>0x100))
		inc=1;
	return sign|((((E+bias)<<23)|((frac>>9)&0x7fffff))+inc);
}
/* 
 * float_twice - Return bit-level equivalent of expression 2*f for
 *   floating point argument f.
 *   Both the argument and result are passed as unsigned int's, but
 *   they are to be interpreted as the bit-level representation of
 *   single-precision floating point values.
 *   When argument is NaN, return argument
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
unsigned float_twice(unsigned uf) {
	unsigned sign=uf>>31;
	unsigned exp=uf>>23&0xff;
	unsigned frac=uf&0x7fffff;
	
	if(exp==0xff&&frac!=0)
		return uf;
	/* denormalized numbers */
	if(exp==0)
	{	
		if((frac&0x400000)==1)
			exp=1;
		frac<<=1;
	}
	/* normalized numbers don't overflow */
	else if(exp<254)
		exp+=1;
	/* normalized numbers overflow */
	else
	{
		exp=0xff;
		frac=0;
	}
	return (sign<<31)|(exp<<23)|frac;
}
